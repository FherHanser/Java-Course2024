public class Numeros {


    public static void main(String[] args) {


        //Variables
        int numeroUno = 456;
        int numeroDos = 654321;
        int numeroTres = 5412;


        //Operación
        int suma = numeroUno + numeroDos + numeroTres;


        //Pantalla
        System.out.println("Los número en el sistema son:  " + numeroUno + " " + numeroDos + " " + numeroTres);
        System.out.println("El resultado de la suma es: " + suma);


    }

}
