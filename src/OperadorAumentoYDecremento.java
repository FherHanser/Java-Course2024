public class OperadorAumentoYDecremento {


    public static void main(String[] args) {

        //Variables
        int x = 5;
        int y = 10;

        //Aumento y Decremento
        x++;
        y--;

        //Salida
        System.out.println("El nuevo valor de x es: " + x);
        System.out.println("El nuevo valor de y es: " + y);



    }
}
